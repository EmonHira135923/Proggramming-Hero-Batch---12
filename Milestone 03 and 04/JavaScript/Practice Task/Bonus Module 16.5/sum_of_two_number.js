/*

Problem 1: Sum of Two Numbers
Description:
Given two integers a and b, return their sum.

Input:
Two integers separated by space.

Output:
One integer — the sum of a and b.

Constraints:
-10^9 ≤ a, b ≤ 10^9

Sample Input:
10 20

Sample Output:
30

Time Limit: 1 second
Memory Limit: 256 MB


*/

function Twovalue(a, b) {
  let sum_value = a + b;
  return sum_value;
}

