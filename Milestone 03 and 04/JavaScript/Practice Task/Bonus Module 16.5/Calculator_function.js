/*

Using Function make a Calculator

*/
// Addition Function
function Add_value(a, b) {
  let sum_value = a + b;
  console.log(sum_value);
}

// Addition input value
let a, b;
a = 50;
b = 50;

// Function Call
Add_value(50, 50);

// Subtraction Function
function Sub_value(a, b) {
  let sum_value = a - b;
  console.log(sum_value);
}

// input value for subtraction
let c, d;
c = 50;
d = 25;

// Function Call
Sub_value(c, d);

// multiplication Function
function mul_value(a, b) {
  let sum_value = a * b;
  console.log(sum_value);
}

// input value

let e, f;
e = 25;
f = 4;

// Function Call
mul_value(e, f);

// Divison Function
function div_value(a, b) {
  let sum_value = a / b;
  console.log(sum_value);
}

// input value
let g, h;
g = 25;
h = 5;

// function call
div_value(g, h);
